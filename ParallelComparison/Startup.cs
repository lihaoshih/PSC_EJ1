        // This method gets called by the runtime. Use this method to add services to the container.
        public void ConfigureServices(IServiceCollection services)
        {
            services.AddMvc().AddNewtonsoftJson(options =>
            {
                options.SerializerSettings.ContractResolver =
                   new DefaultContractResolver();
            });

            services.AddControllers()
                    .AddNewtonsoftJson(ops => { ops.SerializerSettings.NullValueHandling = Newtonsoft.Json.NullValueHandling.Ignore; }); 

            services.AddControllersWithViews().AddJsonOptions(o => { 
                o.JsonSerializerOptions.PropertyNameCaseInsensitive = true;
                o.JsonSerializerOptions.PropertyNamingPolicy = null;
            });

            services.AddRazorPages().AddNewtonsoftJson();
            services.AddTransient<IPLCInfoRepository, PLCInfoRepository>();

            #region Library
            services.AddTransient<IHttpStateCodeService, HttpStateCodeService>();
            services.AddTransient<IShaEncryptService, ShaEncryptService>();
            services.AddTransient<IHttpWebResponseService, HttpWebResponseService>();
            #endregion

            services.AddSession();
		}

